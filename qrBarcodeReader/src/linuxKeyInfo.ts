export type KeyInfo = {
    name: string;
    char: string;
};

export function keyCodeInfo(keyCode: number, shift: boolean): KeyInfo {
    if (shift) {
        return shiftKeys.get(keyCode) || keys.get(keyCode) || { name: 'UNKNOWN', char: '' };
    } else {
        return keys.get(keyCode) || { name: 'UNKNOWN', char: '' };
    }
}

